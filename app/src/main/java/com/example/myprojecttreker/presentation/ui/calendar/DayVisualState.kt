package com.example.myprojecttreker.presentation.ui.calendar

/**
 * Визуальное состояние дня в календаре.
 *
 * Используется для определения того,
 * как конкретный день должен отображаться в UI.
 *
 * Формируется на основе:
 * - наличия задач
 * - их выполнения
 * - положения даты относительно текущего дня
 *
 * Применяется в компонентах:
 * - DayCell
 * - MonthMini
 * - YearScreen
 */
enum class DayVisualState {

    // Все задачи на день выполнены
    Completed,

    // Текущий день (сегодня)
    Today,

    // Будущий день с запланированными задачами
    Future,

    // Прошедший день с незавершёнными задачами
    Past,

    // День без задач
    Empty
}