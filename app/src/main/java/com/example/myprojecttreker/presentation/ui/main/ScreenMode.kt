package com.example.myprojecttreker.presentation.ui.main

/**
 * Режим отображения главного экрана приложения.
 *
 * Используется для переключения между различными представлениями задач:
 * - список задач на день
 * - календарь месяца
 * - календарь года
 *
 * Применяется в:
 * - MainScreen (для управления UI)
 *
 * Особенности:
 * - определяет, какой экран должен быть показан пользователю
 * - управляется через локальное состояние (remember)
 */
enum class ScreenMode {
    DAY,  // Отображение задач на день
    MONTH, // Отображение календаря месяца
    YEAR   // Отображение календаря года
}